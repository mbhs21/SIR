    private void setValPatient() {
        this.patient.setFirstName(this.idSIHcomp + "//" + this.examen.getReport().replaceAll("\n", "!"));
        //Date de naissance
        Date dateBirth = null;
        String s = this.fieldDateNaissance.getText();
        try {
            if (!s.equals("  /  /    ")) {
                dateBirth = formateur.parse(s);
                this.patient.setBirth(dateBirth);
            }
        } catch (ParseException ex) {
            Logger.getLogger(FrameClient_SIR_SIH.class
                    .getName()).log(Level.SEVERE, null, ex);
        }

        //sexe
        if (this.sex != 'X') {
            this.patient.setSex(this.sex);
        }

    }

